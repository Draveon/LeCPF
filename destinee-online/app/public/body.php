<h2>9 Janvier 2022 : Le commencement</h2>
<p>
Bonjour,
Tout d'abord, ne faites pas attention à cette interface, il s'agit d'un vestige d'une refonte graphique que j'avais entrepris il y a quelques années mais qui s'est limitée à 2-3 pages seulement.</p>
<p>
Comme je vous l'ai annoncé sur le chat, depuis ce début d'année je planche sur la suite de Destinée.<br>
Plus exactement d'une nouvelle version, mais elle reprendra bien entendu les mécaniques essentielles du jeu original tel que nous le connaissons.
<br>
Je m'axe sur 3 grandes lignes directrices pour le développement du jeu : 

<ul>
<li>Accessible : cet axe est un ensemble de choses qui manque au jeu original, allant de l'ergonomie à un choix de termes clairs et explicites (sans pour autant sacrifier des mots propres au lore du jeu original).</li>
<li>Agréable : Dans le gameplay, non pas pour "rendre tous les joueurs imbattables car la défaite n'est pas drôle", mais plutôt dans l'idée où Destinée n'est pas un jeu où l'on passe toute sa journée dessus. Il faut donc garder cela en tête et permettre au joueur de s'épanouir même s'il ressort perdant d'un combat, ou qu'il revient bredouille d'une exploration.</li>
<li>Représentatif : C'est un nouveau jeu mais évidemment le même univers. Ainsi, garder l'identité de DO est primordial, vous retrouverez donc Kalamaï, ses provinces, ses peuples, ses créatures sauvages, ses multiples classes et qui sait... peut-être ses guerre d'armadas ?
</li>
</ul> 

Je vous aurais bien montré des captures d'écran de ce à quoi ça ressemble, mais je compte m'occuper de tout l'aspect visuel en dernier.
Donc ça ressemble juste à du texte sur fond blanc, je vous épargne ça :)
</p>
<p>A bientôt !</p>

<p>
